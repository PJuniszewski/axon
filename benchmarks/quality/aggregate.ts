/**
 * Aggregate all quality benchmark results into one report.
 *
 * Reads results/0[1-9]_*.json and writes results/REPORT.md.
 * Missing files mean that test was skipped — report says so explicitly.
 */

import { readFileSync, writeFileSync, existsSync } from "node:fs";
import { resolve, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const R = resolve(__dirname, "results");

function load(file: string): any | null {
  const p = resolve(R, file);
  return existsSync(p) ? JSON.parse(readFileSync(p, "utf-8")) : null;
}

const t1 = load("01_parser_fidelity.json");
const t2 = load("02_entity_preservation.json");
const t3 = load("03_roundtrip_fidelity.json");
const t4 = load("04_hallucination_rate.json");
const t5 = load("05_context_retrieval.json");
const t6 = load("06_multihop_drift.json");
const t7 = load("07_reasoning_quality.json");
const t8 = load("08_native_quality.json");
const t9 = load("09_adversarial.json");

const pct = (x: number) => `${(x * 100).toFixed(1)}%`;
const ppct = (x: number) => `${(x * 100).toFixed(1)} pp`;
const num = (x: number, d = 2) => x.toFixed(d);
const skip = "_(skipped — no data file)_";

let md = `# AXON Quality Benchmark — Results

Generated by \`benchmarks/quality/aggregate.ts\`.
See \`METHODOLOGY.md\` for what each test measures and how scores are
computed. Raw per-sample data lives in \`results/0X_<test>.json\`.

## Headline

| # | Test | Headline | Status |
|---|------|----------|--------|
| 1 | parser_fidelity      | ${t1 ? `${pct(t1.overall.rate)} valid parse (n=${t1.n})` : skip} | ${t1 ? "✓" : "—"} |
| 2 | entity_preservation  | ${t2 ? `${pct(t2.macro.overall)} macro-recall (n=${t2.n})` : skip} | ${t2 ? "✓" : "—"} |
| 3 | roundtrip_fidelity   | ${t3 ? `${num(t3.mean)}/10 mean (n=${t3.n})` : skip} | ${t3 ? "✓" : "—"} |
| 4 | hallucination_rate   | ${t4 ? `${num(t4.hallucinations_per_msg)} added / ${num(t4.info_loss_per_msg)} lost per msg (n=${t4.n})` : skip} | ${t4 ? "✓" : "—"} |
| 5 | context_retrieval    | ${t5 ? `NL ${pct(t5.by_form.NL.accuracy)} vs AXON ${pct(t5.by_form.AXON.accuracy)} (n=${t5.n})` : skip} | ${t5 ? "✓" : "—"} |
| 6 | multihop_drift       | ${t6 ? `hop1 ${num(t6.summary.hop1?.mean ?? 0)} → hop3 ${num(t6.summary.hop3?.mean ?? 0)}` : skip} | ${t6 ? "✓" : "—"} |
| 7 | reasoning_quality    | ${t7 ? `baseline ${pct(t7.arms.baseline.accuracy)} vs codecfit ${pct(t7.arms.codecfit.accuracy)} (Δ ${ppct(t7.delta)})` : skip} | ${t7 ? "✓" : "—"} |
| 8 | native_quality       | ${t8 ? `task ${num(t8.baseline.task_fidelity_mean)}→${num(t8.codecfit.task_fidelity_mean)}, tokens −${num(t8.token_reduction_pct, 1)}%` : skip} | ${t8 ? "✓" : "—"} |
| 9 | adversarial          | ${t9 ? `${pct((t9.counts.correct + t9.counts.ok_hedge) / t9.n)} correct refuse/hedge, ${pct(t9.counts.hallucinated / t9.n)} hallucination (n=${t9.n})` : skip} | ${t9 ? "✓" : "—"} |

---

`;

// Detailed sections
if (t1) {
  md += `## 1. Parser fidelity

**${pct(t1.overall.rate)}** of AXON outputs parsed cleanly (n=${t1.n}).

- Rule-based encoder output: **${pct(t1.by_source.rule_encoder.rate)}** (${t1.by_source.rule_encoder.passed}/${t1.by_source.rule_encoder.total})
- Native LLM samples (synthetic, illustrative): **${pct(t1.by_source.native_llm.rate)}** (${t1.by_source.native_llm.passed}/${t1.by_source.native_llm.total})

> ⚠ The "native LLM samples" set is composed of plausible-looking outputs
> assembled by the benchmark authors, not collected from live API runs.
> Treat the 100% parse rate as a smoke test, not a measurement of how
> often real LLM emissions parse.

`;
}

if (t2) {
  md += `## 2. Entity preservation under rule-based encoding

Macro-averaged recall across **${t2.n}** annotated messages:

| Category    | Recall |
|-------------|-------:|
| Numbers     | ${pct(t2.macro.numbers)} |
| IDs         | ${pct(t2.macro.ids)} |
| Intent      | ${pct(t2.macro.intent)} |
| Qualifiers  | ${pct(t2.macro.qualifiers)} |
| **Overall** | **${pct(t2.macro.overall)}** |

**Failure modes observed:**

`;
  const worst = [...t2.rows].sort((a: any, b: any) => a.overallRecall - b.overallRecall).slice(0, 3);
  for (const w of worst) {
    md += `- \`${w.id}\` (${w.category}, recall ${pct(w.overallRecall)}):\n`;
    md += `  - NL:   \`${w.nl}\`\n`;
    md += `  - AXON: \`${w.axon}\`\n`;
    if (w.hits.numbers.missed.length) md += `  - missed numbers: ${w.hits.numbers.missed.join(", ")}\n`;
    if (w.hits.ids.missed.length) md += `  - missed IDs: ${w.hits.ids.missed.join(", ")}\n`;
    if (w.intentHit === false) md += `  - missed intent: ${w.hits.intent.wanted} (found \`${w.hits.intent.foundSymbol ?? "none"}\` instead of expected symbol)\n`;
    if (w.hits.qualifiers.missed.length) md += `  - missed qualifiers: ${w.hits.qualifiers.missed.join(", ")}\n`;
    md += "\n";
  }
  md += `**Diagnosis.** The rule-based encoder has at least three identifiable
bugs that the headline 46% token-reduction number masks:

1. **Intent ordering bug** — the regex list classifies "Retry the failed
   payment" as ERROR (\`⊗\`) because \`/failed/\` is matched before the
   RETRY rule. Similarly "Deployment complete" lands on REQUEST (\`!\`)
   because \`/deploy/\` fires before the COMPLETE rule.
2. **Decimal-number loss** — \`4.2 percent\` becomes \`42 pct\` after
   punctuation stripping. The number is fundamentally wrong, not just compressed.
3. **Over-aggressive abbreviation** — \`CrashLoopBackOff\` becomes \`Cra\`;
   \`orchestrator\` is sometimes dropped entirely; \`production\` and
   \`kubernetes\` are kept verbatim in some cases but expected as
   \`prod\`/\`k8s\` here, dragging down the ID-recall macro.

`;
}

if (t3) {
  md += `## 3. Round-trip semantic fidelity

NL → encode(rule-based) → AXON → decode(LLM) → NL'
Judged by a separate LLM-as-judge on a 0–10 anchored rubric (strict).

- Mean score: **${num(t3.mean)}/10** [95% CI bootstrap: ${num(t3.ci95[0])}–${num(t3.ci95[1])}]
- Median: **${t3.median}/10**
- Distribution:
  - 9–10: **${t3.buckets["9-10"]}** (${pct(t3.buckets["9-10"] / t3.n)})
  - 7–8:  ${t3.buckets["7-8"]}  (${pct(t3.buckets["7-8"] / t3.n)})
  - 4–6:  ${t3.buckets["4-6"]}  (${pct(t3.buckets["4-6"] / t3.n)})
  - 0–3:  ${t3.buckets["0-3"]}  (${pct(t3.buckets["0-3"] / t3.n)})

**Failure pattern.** Every case with score ≤6 lost a *qualifier* or
condition during round-trip:

- "only fails for EU users, only during peak hours" → "only" dropped twice
- "less than 30 days" → softened to "within 30 days"
- "timeout after 5 seconds if no response" → "if no response" clause dropped
- "all 47 tests" → "47 tests" (universal quantifier dropped)
- "Please" politeness markers consistently dropped

This is the same axis flagged by test 2: qualifiers are the weak link.
Round-trip preserves entities but degrades scope/condition.

`;
}

